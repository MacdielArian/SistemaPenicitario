
class Preso:
    def __init__(self, nombre, nivel_escolaridad, pandilla, annos_condena, asesinato, prestigio):
        self.nombre = nombre
        self.nivel_escolaridad = nivel_escolaridad
        self.pandilla = pandilla
        self.annos_condena = annos_condena
        self.asesinato = asesinato
        self.prestigio = prestigio

    #Setters y getters para nombre
    def set_nombre(self, nombre):
        self.nombre = nombre

    def get_nombre(self):
        return self.nombre

    # Setters y getters para nivel_escolaridad
    def set_nivel_escolaridad(self, nivel):
        self.nivel_escolaridad = nivel

    def get_nivel_escolaridad(self):
        return self.nivel_escolaridad

    # Setters y getters para pandilla
    def set_pandilla(self, pandilla):
        self.pandilla = pandilla

    def get_pandilla(self):
        return self.pandilla

    # Setters y getters para annos_condena
    def set_annos_condena(self, annos):
        self.annos_condena = annos

    def get_annos_condena(self):
        return self.annos_condena

    # Setters y getters para asesinato
    def set_asesinato(self, asesinato):
        self.asesinato = asesinato

    def get_asesinato(self):
        return self.asesinato

    # Setters y getters para prestigio
    def set_prestigio(self, prestigio):
        self.prestigio = prestigio

    def get_prestigio(self):
        return self.prestigio





